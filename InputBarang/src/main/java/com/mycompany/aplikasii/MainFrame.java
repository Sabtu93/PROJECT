    public void simpanKeFile() {
        try (BufferedWriter writer = new  BufferedWriter(new FileWriter(FILE_NAME, true))) {
            writer.write(this.toString());
            writer.newLine();
            System.out.println("Data barang berhasil disimpan.");
            loadBarangFromFile();
        } catch (IOException e) {
            System.out.println("Terjadi kesalahan saat menyimpan data: " + e.getMessage());
        }
    }

    // Metode untuk membaca data barang dari file
    public static void bacaDariFile() {
        try (BufferedReader reader = new BufferedReader(new FileReader(FILE_NAME))) {
            String line;
            while ((line = reader.readLine()) != null) {
                System.out.println(line);
            }
        } catch (IOException e) {
            System.out.println("Terjadi kesalahan saat membaca data: " + e.getMessage());
        }
    }
    

    private void loadBarangFromFile() {
        try (BufferedReader br = new BufferedReader(new FileReader("barang.txt"))) {
            String line;
            while ((line = br.readLine()) != null) {
                String[] data = line.split(" \\| ");
//                if (data.length == 3) {
                    String kode = data[0];
                    String jenis = data[1];
                    String lokasi = data[2];
                    barangList.add(new Barang(kode, jenis, lokasi));
//                }
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
        
    private void saveBarangToFile() {
        try (BufferedWriter bw = new BufferedWriter(new FileWriter("barang.txt"))) {
            for (Barang barang : barangList) {
                bw.write(barang.getKode() + " | " + barang.getJenis() + " | " + barang.getLokasi());
                bw.newLine(); 
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
         
    private void updateTable() {
        DefaultTableModel model = (DefaultTableModel) tbHasil.getModel();
        model.setRowCount(0); // Clear the table
        for (Barang barang : barangList) {
            model.addRow(new Object[]{barang.getKode(), barang.getJenis(), barang.getLokasi()});
        }
    }
           


