    private void hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusActionPerformed
            int selectedRow = tbHasil.getSelectedRow(); // Get the selected row index
    if (selectedRow >= 0) {
        // Confirm deletion
        int confirm = JOptionPane.showConfirmDialog(this, "Apakah Anda yakin ingin menghapus barang ini?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            // Remove the selected item from the list
            barangList.remove(selectedRow);
            // Save updated list to file
            saveBarangToFile(); 
            // Update the table view
            updateTable(); 
        }
    } else {
        JOptionPane.showMessageDialog(this, "Silakan pilih barang yang ingin dihapus.");
    }

    }//GEN-LAST:event_hapusActionPerformed

    private void cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cariActionPerformed
        // TODO add your handling code here:
        String searchTerm = txtSearch.getText().toLowerCase(); // Ambil istilah pencarian
    ArrayList<Barang> filteredList = new ArrayList<>();

    for (Barang barang : barangList) {
        if (barang.getKode().toLowerCase().contains(searchTerm) || 
            barang.getJenis().toLowerCase().contains(searchTerm) || 
            barang.getLokasi().toLowerCase().contains(searchTerm)) {
            filteredList.add(barang); // Tambahkan item yang cocok ke daftar yang difilter
        }
    }

    // Perbarui tabel dengan daftar yang difilter
    DefaultTableModel model = (DefaultTableModel) tbHasil.getModel();
    model.setRowCount(0); // Kosongkan tabel
    for (Barang barang : filteredList) {
        model.addRow(new Object[]{barang.getKode(), barang.getJenis(), barang.getLokasi()});
    }
    }//GEN-LAST:event_cariActionPerformed

    private void urtknActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_urtknActionPerformed
        // TODO add your handling code here:
        Collections.sort(barangList, new Comparator<Barang>() {
        @Override
        public int compare(Barang b1, Barang b2) {
            return b1.getKode().compareTo(b2.getKode()); // Urutkan berdasarkan Kode PO
        }
    });
    saveBarangToFile(); // Simpan daftar yang sudah diurutkan ke file
    updateTable(); // Perbarui tampilan tabel
    }//GEN-LAST:event_urtknActionPerformed

