    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        String kode = txtKode.getText();
        String jenis = txtJenis.getText();
        String lokasi = cmbLok.getSelectedItem().toString();
        Barang barang = new Barang(kode, jenis, lokasi);
        barangList.add(barang);
        saveBarangToFile(); // Save data to file after adding
        updateTable();
        clearFields();
    }//GEN-LAST:event_simpanActionPerformed

    private void ubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ubahActionPerformed
        int selectedRow = tbHasil.getSelectedRow(); // Pastikan tbHasil adalah nama tabel Anda
        if (selectedRow >= 0) {
            String kode = txtKode.getText();
            String jenis = txtJenis.getText();
            String lokasi = cmbLok.getSelectedItem().toString(); // Menggunakan getSelectedItem() bukan getSelectedItems()

            // Update the barang in the list
            Barang updatedBarang = new Barang(kode, jenis, lokasi);
            barangList.set(selectedRow, updatedBarang);

            // Save updated list to file
            saveBarangToFile(); // Save data to file after updating
            updateTable();
            clearFields();
        } else {
            JOptionPane.showMessageDialog(this, "Silakan pilih barang yang ingin diubah.");
        }
    }

    private void clearFields() {
        txtKode.setText("");
        txtJenis.setText("");
        cmbLok.setSelectedIndex(0); // Pastikan 'lokasi' adalah nama JComboBox Anda
    }

