    private void edit1379ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_edit1379ActionPerformed
        // TODO add your handling code here:
        if (indexEdit1379 == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Pilih dulu baris yang ingin diedit!");
            return;
        }
        try {
            String nama = nama1379.getText();
            String nisn = nisn1379.getText();
            String asal = asel1379.getText();
            int nilai = Integer.parseInt(nilai1379.getText());
            
            Siswa1379 s = daftarSiswa1379.get(indexEdit1379);
            s.setNama1379(nama);
            s.setNisn1379(nisn);
            s.setAsal1379(asal);
            s.setNilai1379(nilai);
            
            tableModel.setValueAt(nama, indexEdit1379, 0);
            tableModel.setValueAt(nisn, indexEdit1379, 1);
            tableModel.setValueAt(asal, indexEdit1379, 2);
            tableModel.setValueAt(nilai, indexEdit1379, 3);
            
            javax.swing.JOptionPane.showMessageDialog(this, "Data berhasil diedit!");
            clearFields();
            indexEdit1379 = -1; // Reset agar tidak tertukar edit
        
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Nilai harus angka!");
        }
    }//GEN-LAST:event_edit1379ActionPerformed

