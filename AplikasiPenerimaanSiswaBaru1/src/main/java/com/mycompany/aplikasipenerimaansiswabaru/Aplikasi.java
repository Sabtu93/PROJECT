    private void muatDataDariFile1379() {
    File file = new File("data_siswa.txt");
    if (file.exists()) {
        try (BufferedReader br = new BufferedReader(new FileReader(file))) {
            String line;
            while ((line = br.readLine()) != null) {
                String[] data = line.split(",");
                if (data.length == 4) {
                    String nama = data[0];
                    String nisn = data[1];
                    String asal = data[2];
                    int nilai = Integer.parseInt(data[3]);

                    Siswa1379 siswa = new Siswa1379(nama, nisn, asal, nilai);
                    daftarSiswa1379.add(siswa);
                    tableModel.addRow(new Object[]{nama, nisn, asal, nilai});
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
}
