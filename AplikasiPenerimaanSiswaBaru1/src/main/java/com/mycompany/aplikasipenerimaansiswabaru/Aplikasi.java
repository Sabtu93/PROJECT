    private void cari1379ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cari1379ActionPerformed
        // TODO add your handling code here:
        String keyword = klmcari1379.getText().toLowerCase();
        tableModel.setRowCount(0);
        for (Siswa1379 s : daftarSiswa1379) {
            if (s.getNama1379().toLowerCase().contains(keyword) || s.getNisn1379().contains(keyword)) {
               tableModel.addRow(new Object[]{s.getNama1379(), s.getNisn1379(), s.getAsal1379(), s.getNilai1379()});
            }
        }
        if (keyword.isEmpty()) {
            for (Siswa1379 s : daftarSiswa1379) {
                tableModel.addRow(new Object[]{s.getNama1379(), s.getNisn1379(), s.getAsal1379(), s.getNilai1379()});
            }
            return;
        }
    }//GEN-LAST:event_cari1379ActionPerformed

    private void tmbh1379ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tmbh1379ActionPerformed
        // TODO add your handling code here:
        String nama = nama1379.getText();
        String nisn = nisn1379.getText();
        String asal = asel1379.getText();
        int nilai;
        
        try {
            nilai = Integer.parseInt(nilai1379.getText());
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Nilai harus berupa angka!");
            return;
        }
        
        Siswa1379 siswa = new Siswa1379(nama, nisn, asal, nilai);
        daftarSiswa1379.add(siswa);
        tableModel.addRow(new Object[]{nama, nisn, asal, nilai});
        clearFields();
        
        if (nama.isEmpty() || nisn.isEmpty() || asal.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Semua field harus diisi!");
            return;
}

    }//GEN-LAST:event_tmbh1379ActionPerformed

    private void smpn1379ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_smpn1379ActionPerformed
        // TODO add your handling code here:
        try (BufferedWriter bw = new BufferedWriter(new FileWriter("data_siswa.txt"))) {
            for (Siswa1379 s : daftarSiswa1379) {
                bw.write(s.getNama1379() + "," + s.getNisn1379() + "," + s.getAsal1379() + "," + s.getNilai1379());
                bw.newLine();
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
        javax.swing.JOptionPane.showMessageDialog(this, "Data berhasil disimpan!");
        clearFields();
    }//GEN-LAST:event_smpn1379ActionPerformed

